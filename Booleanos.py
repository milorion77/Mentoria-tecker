## los booleanos representan 1 de 2 valores True or False
## valores Booleanos
## en programacion a menudo necesitamos saber si una expresion es verdadera o falsa
## podemos evaluar cualquier expresion y obtener true o false
##Ejemplos:
print(10<9)##false
print(10<=9)##false
print(10==10)##true
print(32>5)##true
