##evaluar valores y variables booleanos
# bool funcion nos permite evaluar cualquier valor y darle true o false
print(bool("Hola")) #true
print(bool("23"))#true
print(bool(""))#false porque esta vacio
print(bool(["Hola","py"]))#true
print(bool([]))#false porque esta vacio
print(bool(None))#false

